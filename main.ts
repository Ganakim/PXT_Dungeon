function Move (sprite: Sprite, x: number, y: number) {
    if (y != 0 && sprite.vx == 0) {
        if (sprite.vy != y * 50) {
            animation.runImageAnimation(
            sprite,
            animations[1][y + 2],
            200,
            true
            )
        }
        sprite.vy = y * 50
        pause(300)
        sprite.y = Math.round((sprite.y - 8) / 16) * 16 + 8
        if (Math.round(controller.dy(50)) != y) {
            sprite.vy = 0
            animation.runImageAnimation(
            sprite,
            animations[0][y + 2],
            200,
            true
            )
        }
    } else if (x != 0 && sprite.vy == 0) {
        if (sprite.vx != x * 50) {
            animation.runImageAnimation(
            sprite,
            animations[1][x + 1],
            200,
            true
            )
        }
        sprite.vx = x * 50
        pause(300)
        sprite.x = Math.round((sprite.x - 8) / 16) * 16 + 8
        if (Math.round(controller.dx(50)) != x) {
            sprite.vx = 0
            animation.runImageAnimation(
            sprite,
            animations[0][x + 1],
            200,
            true
            )
        }
    }
}
